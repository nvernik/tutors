using System;
using System.Collections.Generic;
using System.Linq;
using System.Web;
using System.Web.Mvc;
using System.Web.Optimization;
using System.Web.Routing;
using Tutors.Models;

namespace Tutors
{
    public class MvcApplication : System.Web.HttpApplication
    {
